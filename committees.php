<h3>Special Events</h3>
<p>Co-Chairs:  Shannah Montgomery and Ani Fischer</p>
<p>This committee is in charge of planning and organizing the movie and skate nights throughout the school year. Movie nights are specifically aimed at Pre-K through 2nd graders. &quot;G&quot; rated movies are selected and the committee solicits volunteers to help pop popcorn for sale along with drinks and other snacks. Skate nights target the older students, grades 3rd - 5th, although many of the older students' younger siblings often attend. Skate nights are held at Skatearound USA located in front of the Satterfield Ball Park. Admission is $3 with a $2 skate rental fee. PTO receives 50% of the admission and rental fee. Food and drinks are available for a nominal fee from Skatearound's concession stand.</p>

